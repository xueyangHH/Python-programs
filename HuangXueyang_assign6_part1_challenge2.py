# function:   maximum
# input:      two numeric values
# processing: compare the two values and determine which one is larger
# output:     return the larger value
def maximum(x,y):
    if x < y:
        return y
    else:
        return x

# function:   minimum
# input:      two numeric values
# processing: compare the two values and determine which one is smaller
# output:     return the smaller value
def minimum(x,y):
    if x < y:
        return x
    else:
        return y
